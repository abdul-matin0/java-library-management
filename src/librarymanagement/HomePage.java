    private void addRecordButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addRecordButtonActionPerformed
        // from addBooksPanel get values from textField and insert into database
        String bookId = bookIdTextField.getText().trim();
        String bookName = bookNameTextField.getText().trim();
        String bookAuthor = bookAuthorTextField.getText().trim();
        String dateAdded = dateAddedTextField.getText().trim();
        
        if(bookId.isEmpty() || bookName.isEmpty() || bookAuthor.isEmpty() || dateAdded.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Field Empty");
        }else{
            
                try {
                con = DriverManager.getConnection(URL, UNAME, UPWD);
                String sql = "INSERT INTO Library_Books VALUES( ?, ?, ?, ?)";
                ps = con.prepareStatement(sql);

                ps.setString(1, bookId);
                ps.setString(2, bookName);
                ps.setString(3, bookAuthor);
                ps.setString(4, dateAdded);

                if(ps.executeUpdate() > 0){
                    //successful
                    JOptionPane.showMessageDialog(rootPane, "Data Saved");
                    bookIdTextField.setText("");
                    bookNameTextField.setText("");
                    bookAuthorTextField.setText("");
                    dateAddedTextField.setText("");
                }

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, ex);
            }

        }
        
    }//GEN-LAST:event_addRecordButtonActionPerformed

