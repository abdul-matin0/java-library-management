    private void modifyRecordButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifyRecordButtonActionPerformed
        // modify new records from modify record panel
        
        String bookId = modifyIdTextField.getText().trim();
        String bookName = modifyNameTextField.getText().trim();
        String bookAuthor = modifyAuthorTextField.getText().trim();
        String newDate = modifyDateTextField.getText().trim();
        
        if(bookId.isEmpty() || bookName.isEmpty() || bookAuthor.isEmpty() || newDate.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Field Empty");
        }else{
            
                try {
                con = DriverManager.getConnection(URL, UNAME, UPWD);
                String sql = "UPDATE Library_books SET bookid = ?, bookname = ?, bookauthor = ?, dateadded = ? WHERE bookid = ?";
                ps = con.prepareStatement(sql);

                ps.setString(1, bookId);
                ps.setString(2, bookName);
                ps.setString(3, bookAuthor);
                ps.setString(4, newDate);
                ps.setString(5, prevId);

                if(ps.executeUpdate() > 0){
                    //successful
                    JOptionPane.showMessageDialog(rootPane, "Data Saved");
                    modifyIdTextField.setText("");
                    modifyNameTextField.setText("");
                    modifyAuthorTextField.setText("");
                    modifyDateTextField.setText("");
                }

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, ex);
            }

        }
        
        
    }//GEN-LAST:event_modifyRecordButtonActionPerformed

