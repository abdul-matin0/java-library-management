    private void getRecordButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getRecordButtonActionPerformed
        // get id and display on modifyRecord panel
        String id = getIdTextField.getText().trim();
        
        //check if id exists and pass to modifyRecord Panel
        if(id.isEmpty()){  
            JOptionPane.showMessageDialog(rootPane, "Field Empty");
        }else{
            try {
                con = DriverManager.getConnection(URL, UNAME, UPWD);
                
                String sql = "SELECT * FROM Library_Books WHERE BookId = ?";
                ps = con.prepareStatement(sql);
                
                ps.setString(1, id);

                rs = ps.executeQuery();
                
                if(rs.next()){
                    //if record exists display modifyrecord Panel
                    
                    modifyIdTextField.setText(rs.getString("bookid"));
                    modifyNameTextField.setText(rs.getString("bookname"));
                    modifyAuthorTextField.setText(rs.getString("bookauthor"));
                    modifyDateTextField.setText(rs.getString("dateadded"));
                    prevId = id;
                    
                    modifyPanel.setVisible(false);
                    modifyRecord.setVisible(true);
                    
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Invalid Book Id");
                }

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, ex);
            }

        }
        
    }//GEN-LAST:event_getRecordButtonActionPerformed

